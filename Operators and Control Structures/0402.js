//This is the "Arithmetic Operators" video tutorial from the "Introducing the JavaScript Language" series. 

//plus operator
2 + 5;

//minus operator 
4 - 3;

//multiplication operator
3 * 5;

//division opeartor
36 / 6; 

//Modulus Operator takes first operand and divides by second and returns remainder of division to find out if even/odd.
20 % 2 //returns 0 
19 % 2 //returns 1

//Incrementing how often you have done something. 
var counter = 0;

counter = counter + 1; //this is one way, but verbose. 

counter += 1; //another way, can implement by any number besides 1. 

counter++; //another way, but incrementing only by 1

//decrementing means your number goes down, instead of up. 
counter -= 1; //will go down by 1
counter--;

//plus also works on strings, but called concatenation and brings together. 
"cat" + "dog"; //would equal catdog

